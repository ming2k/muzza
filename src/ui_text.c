static const uint8_t FALLBACK_GLYPH[7] = { 0x1F, 0x11, 0x05, 0x05, 0x05, 0x11, 0x1F };

static const uint8_t* glyph_rows_for_char(char ch) {
    size_t i = 0;
    unsigned char normalized = (unsigned char)toupper((unsigned char)ch);

    for (i = 0; i < sizeof(UI_GLYPHS) / sizeof(UI_GLYPHS[0]); ++i) {
        if ((unsigned char)UI_GLYPHS[i].ch == normalized) {
            return UI_GLYPHS[i].rows;
        }
    }

    return FALLBACK_GLYPH;
}

static void draw_glyph(fx_canvas* canvas, float x, float y, const uint8_t rows[7], float scale, fx_color color) {
    int row = 0;

    for (row = 0; row < 7; ++row) {
        int col = 0;

        for (col = 0; col < 5; ++col) {
            if ((rows[row] & (1 << (4 - col))) != 0) {
                fx_fill_rect(
                    canvas,
                    &(fx_rect) {
                        x + col * scale,
                        y + row * scale,
                        scale,
                        scale,
                    },
                    color
                );
            }
        }
    }
}

float ui_text_line_height(float scale) {
    return 7.0f * scale;
}

float ui_text_measure(const char* text, float scale) {
    size_t len = 0;

    if (!text || text[0] == '\0') {
        return 0.0f;
    }

    len = strlen(text);
    if (len == 0) {
        return 0.0f;
    }

    return (float)len * (6.0f * scale) - scale;
}

void ui_draw_text(fx_canvas* canvas, float x, float y, const char* text, float scale, fx_color color) {
    size_t i = 0;
    float pen_x = x;

    if (!canvas || !text || scale <= 0.0f) {
        return;
    }

    for (i = 0; text[i] != '\0'; ++i) {
        if (text[i] == '\n') {
            pen_x = x;
            y += ui_text_line_height(scale) + scale;
            continue;
        }

        draw_glyph(canvas, pen_x, y, glyph_rows_for_char(text[i]), scale, color);
        pen_x += 6.0f * scale;
    }
}

void ui_draw_text_centered(fx_canvas* canvas, float x, float y, float w, float h, const char* text, float scale, fx_color color) {
    float text_w = ui_text_measure(text, scale);
    float text_h = ui_text_line_height(scale);

    ui_draw_text(canvas, x + (w - text_w) * 0.5f, y + (h - text_h) * 0.5f, text, scale, color);
}

void ui_draw_text_right(fx_canvas* canvas, float right_x, float y, const char* text, float scale, fx_color color) {
    ui_draw_text(canvas, right_x - ui_text_measure(text, scale), y, text, scale, color);
}

void ui_draw_text_ellipsis(fx_canvas* canvas, float x, float y, float max_width, const char* text, float scale, fx_color color) {
    char clipped[256];
    size_t len = 0;
    size_t keep = 0;
    float ellipsis_w = ui_text_measure("...", scale);

    if (!text) {
        return;
    }

    if (ui_text_measure(text, scale) <= max_width) {
        ui_draw_text(canvas, x, y, text, scale, color);
        return;
    }

    len = strlen(text);
    if (len >= sizeof(clipped)) {
        len = sizeof(clipped) - 1;
    }

    for (keep = 0; keep < len; ++keep) {
        clipped[keep] = text[keep];
        clipped[keep + 1] = '\0';
        if (ui_text_measure(clipped, scale) + ellipsis_w > max_width) {
            break;
        }
    }

    if (keep > 0) {
        clipped[keep] = '\0';
    } else {
        clipped[0] = '\0';
    }

    if (ellipsis_w <= max_width) {
        strncat(clipped, "...", sizeof(clipped) - strlen(clipped) - 1);
    }

    ui_draw_text(canvas, x, y, clipped, scale, color);
}
